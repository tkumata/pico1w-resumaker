def generate_beautiful_font_data():
    """美しいフォントデータを生成"""
    
    # フォントテーブル（元の順序を保持）
    font_table = [
        0x0020,  # スペース文字
        0x3000,  # 全角スペース文字
        0x0030, 0x0031, 0x0032, 0x0033, 0x0034, 0x0035, 0x0036, 0x0037,  # 0-7
        0x0038, 0x0039,  # 8-9
        0x0061, 0x0062, 0x0063, 0x0064, 0x0065, 0x0066,  # a-f
        0x0067, 0x0068, 0x0069, 0x006a, 0x006b, 0x006c, 0x006d, 0x006e,  # g-n
        0x006f, 0x0070, 0x0071, 0x0072, 0x0073, 0x0074, 0x0075, 0x0076,  # o-v
        0x0077, 0x0078, 0x0079, 0x007a,  # w-z
        0x0041, 0x0042, 0x0043, 0x0044,  # A-D
        0x0045, 0x0046, 0x0047, 0x0048, 0x0049, 0x004a, 0x004b, 0x004c,  # E-L
        0x004d, 0x004e, 0x004f, 0x0050, 0x0051, 0x0052, 0x0053, 0x0054,  # M-T
        0x0055, 0x0056, 0x0057, 0x0058, 0x0059, 0x005a,  # U-Z
        0x0021, 0x0040,  # !@
        0x0023, 0x0024, 0x0025, 0x005e, 0x0026, 0x002a, 0x0028, 0x0029,  # #$%^&*()
        0x005f, 0x002b, 0x002d, 0x003d, 0x005b, 0x005d, 0x007b, 0x007d,  # _+-=[]{}
        0x007c, 0x003b, 0x0027, 0x003a, 0x0022, 0x002c, 0x002e, 0x002f,  # |;':",./
        0x003c, 0x003e, 0x003f,  # <>?
    ]
    
    all_font_data = []
    
    for code in font_table:
        char = chr(code)
        
        # 特別なケース
        if code == 0x3000:  # 全角スペース
            char = ' '
        
        # パターンが定義されている場合
        if char in FONT_PATTERNS:
            font_bytes = pattern_to_bytes(FONT_PATTERNS[char])
            all_font_data.extend(font_bytes)
        else:
            # パターンが定義されていない場合はデフォルト（?）
            if '?' in FONT_PATTERNS:
                font_bytes = pattern_to_bytes(FONT_PATTERNS['?'])
                all_font_data.extend(font_bytes)
            else:
                # 最後の手段：空のフォント
                all_font_data.extend([0] * 15)
    
    return tuple(all_font_data), tuple(font_table)

# データ生成
lite_font_data, lite_font_table = generate_beautiful_font_data()