def pattern_to_bytes(pattern_str):
    """パターン文字列からバイトデータに変換"""
    lines = pattern_str.strip().split('\n')
    font_data = []
    
    for line in lines:
        if len(line) >= 7:
            byte_val = 0
            for i in range(7):
                if i < len(line) and line[i] == '█':
                    byte_val |= (1 << (7-i-1))
            font_data.append(byte_val)
    
    return font_data

